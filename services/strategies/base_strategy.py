    async def calculate_indicators(self, symbol: str, timeframe: str) -> Optional[MarketSnapshot]:
        """
        Get indicators as MarketSnapshot for snapshot consistency.
        
        Returns:
            MarketSnapshot if available, None otherwise
        """
        bundle_key = (symbol, timeframe)
        from core.container import container
        import time
        try:
            se = container.get("strategy_engine")
            if se and hasattr(se, "_indicator_snapshots"):
                # [RACE-PROOF] Log cache read with microsecond timestamp
                cache_read_timestamp_us = time.time()
                
                # [FIX CACHE RACE CONDITION] ONLY retrieve timestamp-keyed cache to prevent overwrites
                # First try to get the latest timestamp from the regular cache
                cached = se._indicator_snapshots.get(bundle_key)
                cache_object_id = id(cached) if cached else None
                
                if cached:
                    signal_candle_ts = cached.get("signal_candle_ts", 0)
                    cached_crossover = cached.get("crossover_detected", "NOT FOUND")
                    logger.debug(f"[RACE-PROOF] CACHE READ (regular): ts_us={cache_read_timestamp_us:.6f}, key={bundle_key}, signal_candle_ts={signal_candle_ts}, crossover={cached_crossover}, object_id={cache_object_id}")
                    
                    if signal_candle_ts > 0:
                        timestamp_key = (symbol, timeframe, signal_candle_ts)
                        timestamp_cached = se._indicator_snapshots.get(timestamp_key)
                        timestamp_object_id = id(timestamp_cached) if timestamp_cached else None
                        timestamp_crossover = timestamp_cached.get("crossover_detected", "NOT FOUND") if timestamp_cached else "NOT FOUND"
                        logger.debug(f"[RACE-PROOF] CACHE READ (timestamp-keyed): ts_us={cache_read_timestamp_us:.6f}, key={timestamp_key}, exists={timestamp_cached is not None}, crossover={timestamp_crossover}, object_id={timestamp_object_id}")
                        
                        if timestamp_cached:
                            logger.debug(f"[RACE-PROOF] USING TIMESTAMP-KEYED CACHE: ts_us={cache_read_timestamp_us:.6f}, crossover={timestamp_crossover}")
                            cached = timestamp_cached
                    
                    # Create snapshot from cached indicators
                    if isinstance(cached, MarketSnapshot):
                        return cached
                    elif isinstance(cached, dict):
                        # Create a MarketSnapshot from dict (temporary solution)
                        # Calculate dynamic reference_candle_index based on timeframe's confirmation candles
                        # SUPPORT BOTH MODES: 0=Realtime (forming candle, -1), >=1=Confirmation (closed candle, -2)
                        timeframe_confirmation_map = {
                            "5m": settings.confirmation_candles_5m,
                            "15m": settings.confirmation_candles_15m,
                            "1H": settings.confirmation_candles_1h,
                            "4H": settings.confirmation_candles_4h,
                            "1D": settings.confirmation_candles_1d,
                            "1W": settings.confirmation_candles_1w,
                            "1M": settings.confirmation_candles_1m,
                        }
                        if timeframe not in timeframe_confirmation_map:
                            logger.error(f"UNKNOWN_TIMEFRAME: {timeframe}")
                            raise RuntimeError(f"UNKNOWN_TIMEFRAME: {timeframe}")
                        confirm_candles = timeframe_confirmation_map[timeframe]
                        ref_index = -2 if confirm_candles >= 1 else -1
                        candle_type = "closed" if confirm_candles >= 1 else "forming"
                        # Create snapshot - all values pre-validated by MarketDataEngine signal gate
                        # PRODUCTION HARD RULES are enforced centrally in MarketSnapshot.create()
                        snapshot = MarketSnapshot.create(
                            symbol=symbol,
                            timeframe=timeframe,
                            reference_candle_timestamp=cached.get("signal_candle_ts", 0),
                            reference_candle_index=ref_index,
                            candle_type=candle_type,
                            ema_fast=cached.get("ema9", 0.0),
                            ema_slow=cached.get("ema21", 0.0),
                            adx=cached.get("adx", 0.0),
                            body_pct=cached.get("body_pct", 0.0),
                            indicators=copy.deepcopy(cached),  # SHARED REFERENCE ELIMINATION: Deep copy to prevent mutation
                            raw_data={
                                "close": cached.get("close", 0.0),
                                "open": cached.get("open", 0.0),
                                "high": cached.get("high", 0.0),
                                "low": cached.get("low", 0.0),
                                "volume": cached.get("volume", 0.0),
                            },  # Extract OHLCV from cached indicators for entry price
                        )
                        # Redundant safety check - will never fail in normal operation
                        snapshot.validate_consistency()
                        logger.info(
                            f"[SNAPSHOT-VALID] Strategy layer cache snapshot created for {symbol}/{timeframe}: "
                            f"candle_type={snapshot.candle_type}, ref_index={snapshot.reference_candle_index}"
                        )
                        # --- End of snapshot creation ---
                        logger.debug(
                            f"[MUTATION-CACHE-READ] id(cached)={id(cached)}, "
                            f"cached['body_pct']={cached.get('body_pct', 'NOT_SET')}, "
                            f"snapshot.body_pct={snapshot.body_pct}, "
                            f"id(snapshot.indicators)={id(snapshot.indicators)}, "
                            f"SHARED_REFERENCE={'YES' if id(cached) == id(snapshot.indicators) else 'NO'}"
                        )
                        # --- RUNTIME VALIDATION: Ensure snapshot consistency per 4_chien_luoc.md v1.2 ---
                        if not snapshot.validate_consistency():
                            logger.error(
                                f"[STRATEGY-COMPLIANCE] Snapshot consistency validation FAILED for {symbol}/{timeframe}: "
                                f"candle_type={snapshot.candle_type}, ref_index={snapshot.reference_candle_index}"
                            )
                        return snapshot
        except Exception as e:
            logger.exception(
                f"[BASE_STRATEGY_CACHED_BRANCH_ERROR] "
                f"{symbol}/{timeframe}: {e}"
            )

        try:
            mde = container.get("market_data_engine")
            import inspect, asyncio

            if inspect.isawaitable(mde) or asyncio.iscoroutine(mde):
                logger.debug("MDE instance is awaitable/coroutine; falling back to local indicators")
            elif mde:
                # MDE.get_indicators returns Dict for backward compatibility
                # We need to convert to MarketSnapshot
                indicators = mde.get_indicators(symbol, timeframe)
                if indicators:
                    # SUPPORT BOTH MODES: 0=Realtime (forming candle, -1), >=1=Confirmation (closed candle, -2)
                    timeframe_confirmation_map = {
                        "5m": settings.confirmation_candles_5m,
                        "15m": settings.confirmation_candles_15m,
                        "1H": settings.confirmation_candles_1h,
                        "4H": settings.confirmation_candles_4h,
                        "1D": settings.confirmation_candles_1d,
                        "1W": settings.confirmation_candles_1w,
                        "1M": settings.confirmation_candles_1m,
                    }
                    if timeframe not in timeframe_confirmation_map:
                        logger.error(f"UNKNOWN_TIMEFRAME: {timeframe}")
                        raise RuntimeError(f"UNKNOWN_TIMEFRAME: {timeframe}")
                    confirm_candles = timeframe_confirmation_map[timeframe]
                    # Create snapshot - all values pre-validated by MarketDataEngine signal gate
                    # PRODUCTION HARD RULES are enforced centrally in MarketSnapshot.create()
                    # Use per-timeframe confirmation settings from configuration
                    confirm_candles = timeframe_confirmation_map[timeframe]
                    ref_index = -2 if confirm_candles >= 1 else -1
                    candle_type = "closed" if confirm_candles >=1 else "forming"
                    snapshot = MarketSnapshot.create(
                        symbol=symbol,
                        timeframe=timeframe,
                        reference_candle_timestamp=indicators.get("signal_candle_ts", 0),
                        reference_candle_index=ref_index,
                        candle_type=candle_type,
                        ema_fast=indicators.get("ema9", 0.0),
                        ema_slow=indicators.get("ema21", 0.0),
                        adx=indicators.get("adx", 0.0),
                        body_pct=indicators.get("body_pct", 0.0),
                        indicators=indicators,
                    )
                    # Redundant safety check - will never fail in normal operation
                    snapshot.validate_consistency()
                    logger.info(
                        f"[SNAPSHOT-VALID] Strategy layer snapshot created for {symbol}/{timeframe}: "
                        f"candle_type={snapshot.candle_type}, ref_index={snapshot.reference_candle_index}"
                        )
                    return snapshot
        except Exception as e:
            logger.error(f"Failed to get indicators from MDE for {symbol} {timeframe}: {e}")

        return None

