    def get_candles(self, symbol: str, timeframe: str, limit: int = 100) -> List[OHLCV]:
        """Get recent candles for analysis — prefer atomic snapshot from StrategyEngine."""
        from core.container import container
        try:
            se = container.get("strategy_engine")
            if se and hasattr(se, "_analysis_snapshots"):
                snap = se._analysis_snapshots.get((symbol, timeframe))
                if snap:
                    return list(snap)[-limit:]
        except Exception as e:
            logger.exception(
                f"[GET_CANDLES_ERROR] "
                f"{symbol}/{timeframe}: {e}"
            )

        try:
            mde = container.get("market_data_engine")
            import inspect, asyncio

            if inspect.isawaitable(mde) or asyncio.iscoroutine(mde):
                logger.critical("[ARCHITECTURE VIOLATION] MDE is awaitable, cannot fetch candles safely!")
                return []
            elif mde:
                snapshot = mde.get_candles_snapshot(symbol, timeframe, limit)
                if snapshot:
                    return snapshot
        except Exception as e:
            logger.error(f"Failed to get candles from MDE for {symbol} {timeframe}: {e}")

        return []

