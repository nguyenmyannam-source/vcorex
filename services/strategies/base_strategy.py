class SignalType(Enum):
    """Types of trading signals."""

    BUY = "buy"
    SELL = "sell"
    HOLD = "hold"
    CLOSE = "close"


class SignalStrength(Enum):
    """Signal confidence level."""

    LOW = 1
    MEDIUM = 2
    HIGH = 3
    VERY_HIGH = 4
    EXTREME = 5


@dataclass
class Signal:
    """Trading signal generated by a strategy."""

    signal_id: str = field(default_factory=lambda: str(uuid4()))
    strategy_name: str = ""
    symbol: str = ""
    timeframe: str = ""
    signal_type: SignalType = SignalType.HOLD
    signal_strength: SignalStrength = SignalStrength.LOW
    entry_price: float = 0.0
    stop_loss_price: Optional[float] = None
    take_profit_prices: List[Dict[str, float]] = field(default_factory=list)
    position_size_usdt: float = 0.0
    reason: str = ""
    indicators: Dict[str, Any] = field(default_factory=dict)
    timestamp: datetime = field(default_factory=lambda: datetime.now(timezone.utc))
    validated: bool = False
    risk_approved: bool = False
    snapshot: Optional[MarketSnapshot] = None

    def __post_init__(self):
        """Validate required fields after initialization."""
        if not self.symbol:
            raise ValueError("Signal.symbol is required")
        if not self.timeframe:
            raise ValueError("Signal.timeframe is required")
        if self.entry_price <= 0:
            raise ValueError("Signal.entry_price must be positive")
        if self.signal_type == SignalType.HOLD:
            raise ValueError("Signal.signal_type cannot be HOLD")


@dataclass
class StrategyConfig:
    """Base configuration for all strategies."""

    name: str
    enabled: bool = True
    symbols: List[str] = field(default_factory=list)
    timeframes: List[str] = field(default_factory=list)
    min_confidence: SignalStrength = SignalStrength.MEDIUM
    max_positions: int = 5
    cooldown_minutes: int = None  # Will use settings.cooldown_minutes if None


class BaseStrategy(ABC):
    """
    Abstract base class for all trading strategies.
    Defines the interface that strategy engine expects from all strategies.
    """

    def __init__(self, config: StrategyConfig):
        self.config = config
        self.indicators: Dict[str, Dict[str, Any]] = {}  # symbol -> {indicator: value}
        # [PHASE 11] SHADOW BUFFER ELIMINATED: Strategies must rely entirely on MarketDataEngine
        logger.info(f"Strategy {self.config.name} initialized")

    @abstractmethod
    async def generate_signal(self, symbol: str, timeframe: str) -> Optional[Signal]:
        """
        Generate a trading signal based on current market data.
        This is the main strategy logic that analyzes data and produces signals.
        """
        pass

    @abstractmethod
    async def validate_signal(self, signal: Signal) -> bool:
        """
        Validate that a signal meets all strategy criteria before sending to risk engine.
        Performs additional checks to filter out false positives.
        """
        pass

    @abstractmethod
    async def filters(self, symbol: str, timeframe: str) -> bool:
        """
        Apply pre-analysis filters to determine if the symbol/timeframe should even be analyzed.
        Can be used for liquidity checks, volatility filters, etc.
        """
        pass

    @abstractmethod
    async def build_trade_plan(self, signal: Signal) -> Signal:
        """
        Build complete trade plan from a raw signal including position sizing, TP/SL levels.
        Enhances the basic signal with execution parameters.
        """
        pass

